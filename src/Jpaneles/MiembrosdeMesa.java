    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Main sistema = Main.getInstancia();
        int indice = jComboBox1.getSelectedIndex();
        String dato = jTextField6.getText();

        try {
            int numCambiar = Integer.parseInt(jTextField7.getText())-1;
            if (numCambiar >= 0 && numCambiar < sistema.miembrosMesa.size()) {
                if (indice == 0) {
                    sistema.miembrosMesa.get(numCambiar).setNombres(dato);
                } else if (indice ==1) {
                    sistema.miembrosMesa.get(numCambiar).setApellidos(dato);
                } else if (indice ==2) {
                    sistema.miembrosMesa.get(numCambiar).setTipoMiembro(dato);
                }
                // cambiarn en la tabla
                DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
                modelo.setValueAt(dato, numCambiar, indice);
                
                JOptionPane.showMessageDialog(null, "Campo actualizado correctamente");
                jTextField6.setText("");
                jTextField7.setText("");
                
            } else {
                JOptionPane.showMessageDialog(null, "Número fuera de rango");                
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingresa un número válido");
        }  
    }//GEN-LAST:event_jButton3ActionPerformed

