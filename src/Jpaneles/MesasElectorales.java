    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Main sistema = Main.getInstancia();
        int indice = jComboBox1.getSelectedIndex();
        String dato = jTextField6.getText();

        try {
            int numCambiar = Integer.parseInt(jTextField7.getText())-1;
            if (numCambiar >= 0 && numCambiar < sistema.candidatos.size()) {
                if (indice == 0) {
                    sistema.mesasElectorales.get(numCambiar).setIdentificador(dato);
                } else if (indice ==1) {
                    sistema.mesasElectorales.get(numCambiar).setUbicacion(dato);
                } 
                
                JOptionPane.showMessageDialog(null, "Campo actualizado correctamente");
                jTextField6.setText("");
                jTextField7.setText("");
                
            } else {
                JOptionPane.showMessageDialog(null, "Número fuera de rango");                
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingresa un número válido");
        }  
    }//GEN-LAST:event_jButton3ActionPerformed

