    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String ubicacion = jTextField2.getText();

        Main sistema = Main.getInstancia();

        sistema.agregarMesasElectorales(id, ubicacion);

        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.addRow(new Object[]{id, ubicacion, "lista"});


        jTextField1.setText("");
        jTextField2.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Main sistema = Main.getInstancia();
        try {
            int numBorrar = Integer.parseInt(jTextField5.getText());
            boolean comprobante =sistema.DeleteCandidato(numBorrar);
            if(comprobante){
                JOptionPane.showMessageDialog(null, "Se borro con exito");
                DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
                modelo.removeRow(numBorrar - 1);
                jTextField5.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "No se encontro tal registro");

            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingresa un número válido");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

